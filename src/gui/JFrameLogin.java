    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String login = campoUsuario.getText().trim();
        String password = campoPassword.getText().trim();
        if (login.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                    "Debe especificar usuario y contrase침a", "Aviso", 
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        DAOUsuarios du = new DAOUsuarios();
        Usuario usuario = du.buscarPorLogin(login);
        if (usuario == null || !password.equals(usuario.getPassword())) {
            JOptionPane.showMessageDialog(this, "Usuario o contrase침a incorrectos",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } else if ("admin".equalsIgnoreCase(usuario.getRol())) {
            JFrameAdmin adminFrame = new JFrameAdmin();
            dispose();
            adminFrame.setVisible(true);
        } else {
            JFrameReserva newframe = new JFrameReserva();
            dispose();
            newframe.setVisible(true);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

